namespace core\event;

defined('MOODLE_INTERNAL') || die();

/**
 * Report viewed event class.
 *
 * This is an abstract to guide the developers in using this event name for their events.
 * It is intended to be used when a report is viewed within Moodle.
 *
 * @package    core
 * @copyright  2013 Mark Nelson <markn@moodle.com>
 * @license    http://www.gnu.org/copyleft/gpl.html GNU GPL v3 or later
 */
abstract class report_viewed extends base {

    /**
     * Init method.
     */
    protected function init() {
        $this->data['crud'] = 'r';
        $this->data['level'] = self::LEVEL_OTHER;
    }

    /**
     * Return localised event name.
     *
     * @return string
     */
    public static function get_name() {
        return get_string('eventreportviewed');
    }

    /**
     * Returns description of what happened.
     *
     * @return string
     */
    public function get_description() {
        return 'The \'' . $this->component . '\' report was viewed by the user with the id ' . $this->userid;
    }
}
